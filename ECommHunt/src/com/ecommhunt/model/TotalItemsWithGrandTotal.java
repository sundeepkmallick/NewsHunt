package com.ecommhunt.model;

public class TotalItemsWithGrandTotal {
	int totalItems;
	float grandTotal;
	float actualGrandTotal;
	
	public int getTotalItems() {
		return totalItems;
	}
	public void setTotalItems(int totalItems) {
		this.totalItems = totalItems;
	}
	
	public float getGrandTotal() {
		return grandTotal;
	}
	public void setGrandTotal(float grandTotal) {
		this.grandTotal = grandTotal;
	}
	
	public float getActualGrandTotal() {
		return actualGrandTotal;
	}
	public void setActualGrandTotal(float actualGrandTotal) {
		this.actualGrandTotal = actualGrandTotal;
	}
	
	
}
